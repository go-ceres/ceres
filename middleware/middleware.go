package middleware

import "context"

// Handler 定义中间调用的处理函数
type Handler func(ctx context.Context, req interface{}) (interface{}, error)

// Middleware 中间件定义
type Middleware func(Handler) Handler

// Chain 中间件执行通道
func Chain(m ...Middleware) Middleware {
	return func(next Handler) Handler {
		for i := len(m) - 1; i >= 0; i-- {
			next = m[i](next)
		}
		return next
	}
}
